package Model;

/**
 *
 * @author ariel
 */
public class User {
    private String dni;
    private String name;
    private String lastName;
    private int age;

    public User() {
    }

    public User(String dni, String name, String lastName, int age) {
        this.dni = dni;
        this.name = name;
        this.lastName = lastName;
        this.age = age;
    }

    public String getDni() {
        return dni;
    }

    public void setDni(String dni) {
        this.dni = dni;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

   

    
}
